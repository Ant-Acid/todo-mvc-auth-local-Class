const mongoose = require('mongoose') // import mongoose

const TodoSchema = new mongoose.Schema({ // create todo schema
  todo: {
    type: String,
    required: true,
  },
  completed: {
    type: Boolean,
    required: true,
  },
  userId: {
    type: String,
    required: true
  }
})

module.exports = mongoose.model('Todo', TodoSchema) // export todo model
